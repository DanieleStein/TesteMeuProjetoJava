package br.com.aulas;

public class Aula_22 {
	
	public static void main(String[] args) {
		
		
		// Loop While usado para repetir um comando enquanto a informação for verdadeira
		// Quando usado o ++i, ele vai repetir e incrementar mais 1 até chegar em 101, caso contrário ele ficaria imprimindo o número 1 infinitamente.
		
		int i = 79;
		
		while (i < 101) {
			System.out.println(i);
			++i;
		}
		
		
		
	}

}
